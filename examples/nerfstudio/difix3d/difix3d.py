from __future__ import annotations

from dataclasses import dataclass, field
from typing import Type

import torch
from nerfstudio.models.nerfacto import NerfactoModel, NerfactoModelConfig
from difix3d.difix3d_field import Difix3DField
from nerfstudio.cameras.camera_optimizers import CameraOptimizer
from nerfstudio.field_components.spatial_distortions import SceneContraction
from nerfstudio.fields.density_fields import HashMLPDensityField


@dataclass
class Difix3DModelConfig(NerfactoModelConfig):
  """Configuration for the Difix3DModel."""

  _target: Type = field(default_factory=lambda: Difix3DModel)

  disable_camera_optimizer: bool = False
  """Whether to disable the camera optimizer or not."""
  freeze_appearance_embedding: bool = False


class Difix3DModel(NerfactoModel):
  """Model for Difix3D."""

  config: Difix3DModelConfig

  def populate_modules(self):
    """Set the fields and modules."""
    super().populate_modules()

    if self.config.disable_scene_contraction:
      scene_contraction = None
    else:
      scene_contraction = SceneContraction(order=float("inf"))

    appearance_embedding_dim = (
      self.config.appearance_embed_dim if self.config.use_appearance_embedding else 0
    )

    # Fields
    self.field = Difix3DField(
      self.scene_box.aabb,
      hidden_dim=self.config.hidden_dim,
      num_levels=self.config.num_levels,
      max_res=self.config.max_res,
      base_res=self.config.base_res,
      features_per_level=self.config.features_per_level,
      log2_hashmap_size=self.config.log2_hashmap_size,
      hidden_dim_color=self.config.hidden_dim_color,
      hidden_dim_transient=self.config.hidden_dim_transient,
      spatial_distortion=scene_contraction,
      num_images=self.num_train_data,
      use_pred_normals=self.config.predict_normals,
      use_average_appearance_embedding=self.config.use_average_appearance_embedding,
      appearance_embedding_dim=appearance_embedding_dim,
      average_init_density=self.config.average_init_density,
      implementation=self.config.implementation,
      freeze_appearance_embedding=self.config.freeze_appearance_embedding,
    )

    self.camera_optimizer: CameraOptimizer = self.config.camera_optimizer.setup(
      num_cameras=self.num_train_data, device="cpu"
    )
    if self.config.disable_camera_optimizer:
      self.camera_optimizer.mode = "off"
    self.density_fns = []
    num_prop_nets = self.config.num_proposal_iterations
    # Build the proposal network(s)
    self.proposal_networks = torch.nn.ModuleList()
    if self.config.use_same_proposal_network:
      assert len(self.config.proposal_net_args_list) == 1, "Only one proposal network is allowed."
      prop_net_args = self.config.proposal_net_args_list[0]
      network = HashMLPDensityField(
        self.scene_box.aabb,
        spatial_distortion=scene_contraction,
        **prop_net_args,
        average_init_density=self.config.average_init_density,
        implementation=self.config.implementation,
      )
      self.proposal_networks.append(network)
      self.density_fns.extend([network.density_fn for _ in range(num_prop_nets)])
    else:
      for i in range(num_prop_nets):
        prop_net_args = self.config.proposal_net_args_list[
          min(i, len(self.config.proposal_net_args_list) - 1)
        ]
        network = HashMLPDensityField(
          self.scene_box.aabb,
          spatial_distortion=scene_contraction,
          **prop_net_args,
          average_init_density=self.config.average_init_density,
          implementation=self.config.implementation,
        )
        self.proposal_networks.append(network)
      self.density_fns.extend([network.density_fn for network in self.proposal_networks])
